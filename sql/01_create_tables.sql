-- LOG_INSPECTION: 검사 로그
CREATE TABLE LOG_INSPECTION (
    LOG_ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQUIPMENT_ID      VARCHAR2(50)  NOT NULL,
    LOG_TIMESTAMP     TIMESTAMP     DEFAULT SYSTIMESTAMP,
    INSPECT_ITEM      VARCHAR2(200),
    INSPECT_VALUE     VARCHAR2(200),
    INSPECT_UNIT      VARCHAR2(50),
    INSPECT_RESULT    VARCHAR2(100),
    CREATED_AT        TIMESTAMP     DEFAULT SYSTIMESTAMP
);

COMMENT ON TABLE LOG_INSPECTION IS '설비 검사 로그 (파이프 구분 형식)';

-- LOG_ALARM: 알람 로그
CREATE TABLE LOG_ALARM (
    LOG_ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQUIPMENT_ID      VARCHAR2(50)  NOT NULL,
    LOG_TIMESTAMP     TIMESTAMP     DEFAULT SYSTIMESTAMP,
    ALARM_CODE        VARCHAR2(50),
    ALARM_LEVEL       VARCHAR2(20),
    ALARM_MESSAGE     VARCHAR2(4000),
    CREATED_AT        TIMESTAMP     DEFAULT SYSTIMESTAMP
);

COMMENT ON TABLE LOG_ALARM IS '설비 알람 로그 (JSON 형식)';

-- LOG_PROCESS: 공정 로그
CREATE TABLE LOG_PROCESS (
    LOG_ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQUIPMENT_ID      VARCHAR2(50)  NOT NULL,
    LOG_TIMESTAMP     TIMESTAMP     DEFAULT SYSTIMESTAMP,
    PROCESS_STEP      VARCHAR2(200),
    STEP_STATUS       VARCHAR2(50),
    START_TIME        VARCHAR2(100),
    END_TIME          VARCHAR2(100),
    DURATION_MS       VARCHAR2(50),
    CREATED_AT        TIMESTAMP     DEFAULT SYSTIMESTAMP
);

COMMENT ON TABLE LOG_PROCESS IS '공정 단계 로그 (CSV 형식)';
