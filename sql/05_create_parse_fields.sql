CREATE TABLE VRL_PARSE_FIELDS (
    FIELD_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQUIPMENT_TYPE  VARCHAR2(50)  NOT NULL,   -- SP, AOI, REFLOW 등
    FIELD_NAME      VARCHAR2(200) NOT NULL,   -- data.INSPECTOR
    FIELD_LABEL     VARCHAR2(200),            -- 표시명
    FIELD_ORDER     NUMBER        DEFAULT 0,
    CREATED_AT      TIMESTAMP     DEFAULT SYSTIMESTAMP,
    CONSTRAINT UK_EQUIP_FIELD UNIQUE (EQUIPMENT_TYPE, FIELD_NAME)
);

CREATE INDEX IDX_PARSE_EQUIP_TYPE ON VRL_PARSE_FIELDS(EQUIPMENT_TYPE);

