CREATE TABLE TABLE_COLUMN_REGISTRY (
    REGISTRY_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TABLE_NAME    VARCHAR2(100)  NOT NULL,
    COLUMN_NAME   VARCHAR2(100)  NOT NULL,
    DATA_TYPE     VARCHAR2(50)   NOT NULL,
    IS_REQUIRED   VARCHAR2(1)    DEFAULT 'N',
    COLUMN_ORDER  NUMBER         NOT NULL,
    DESCRIPTION   VARCHAR2(500),
    CREATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP,
    UPDATED_AT    TIMESTAMP      DEFAULT SYSTIMESTAMP,
    CONSTRAINT UK_TABLE_COLUMN UNIQUE (TABLE_NAME, COLUMN_NAME)
);

