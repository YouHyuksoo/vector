--  4) 트리거는 trg_is_last_iscm_ict.sql 로 별도 적용
--  멱등: DROP 은 없는 객체 무시. 재실행 안전(단, 데이터는 초기화됨).
-- =============================================================================

-- ── 1) 기존 3테이블 DROP ──
BEGIN
  FOR t IN (SELECT column_value AS tab FROM TABLE(sys.odcivarchar2list(
              'LOG_ISCM_ICT', 'LOG_ISCM_ICT_HDATA', 'LOG_ISCM_ICT_COMP'))) LOOP
    BEGIN
      EXECUTE IMMEDIATE 'DROP TABLE ' || t.tab || ' CASCADE CONSTRAINTS PURGE';
      DBMS_OUTPUT.PUT_LINE('DROPPED: ' || t.tab);
    EXCEPTION
      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN RAISE; END IF;  -- ORA-00942: 테이블 없음 → 무시
        DBMS_OUTPUT.PUT_LINE('SKIP (no table): ' || t.tab);
    END;
  END LOOP;
END;
/

