-- ── 3) 작업일자 컬럼 추가 (LOG_ICT 정의 복사, 트리거 전용) ──
DECLARE
  TYPE t_names IS TABLE OF VARCHAR2(128);
  v_cols t_names := t_names('ACTUAL_DATE', 'SHIFT_CODE', 'ZONE_CODE');
  v_type   VARCHAR2(200);
  v_exists NUMBER;
BEGIN
  FOR j IN 1 .. v_cols.COUNT LOOP
    SELECT COUNT(*) INTO v_exists
      FROM user_tab_columns
     WHERE table_name = 'LOG_ISCM_ICT' AND column_name = v_cols(j);
    IF v_exists > 0 THEN CONTINUE; END IF;

    SELECT CASE data_type
             WHEN 'VARCHAR2' THEN 'VARCHAR2(' || data_length || ')'
             WHEN 'CHAR'     THEN 'CHAR(' || data_length || ')'
             WHEN 'NUMBER'   THEN
               CASE WHEN data_precision IS NULL THEN 'NUMBER'
                    ELSE 'NUMBER(' || data_precision || ',' || NVL(data_scale, 0) || ')'
               END
             ELSE data_type
           END
      INTO v_type
      FROM user_tab_columns
     WHERE table_name = 'LOG_ICT' AND column_name = v_cols(j);

    EXECUTE IMMEDIATE 'ALTER TABLE LOG_ISCM_ICT ADD (' || v_cols(j) || ' ' || v_type || ')';
    DBMS_OUTPUT.PUT_LINE('ADDED: LOG_ISCM_ICT.' || v_cols(j) || ' ' || v_type);
  END LOOP;
END;
/

