/**
 * @file src/database/oracle-ddl.ts
 * @description Oracle DDL 자동 생성 빌더 — 테이블/프로시져 DDL + 타입 추론 + 에러 파서
 *
 * 초보자 가이드:
 * 1. **fieldToColumnName**: VRL 파싱 필드명(data.INSPECTOR)을 Oracle 컬럼명(INSPECTOR)으로 변환
 * 2. **inferOracleType**: 필드명 패턴으로 Oracle 데이터 타입 자동 추론
 * 3. **buildCreateTableDDL**: 테이블 CREATE DDL 문 생성
 * 4. **buildCreateProcedureDDL**: INSERT 프로시져 CREATE DDL 문 생성
 * 5. **buildRegistryColumns / buildRegistryProcedure**: 자동 매핑용 registry 데이터 생성
 * 6. **parseOracleError**: ORA 에러 코드를 사용자 메시지로 변환
 */

import type { RegistryColumn, ProcedureEntry, ProcedureParam } from '../config/local-registry.js';

/** 필드 정의 (API 요청에서 전달) */
export interface FieldDef {
  fieldName: string;
  fieldLabel: string;
}

/** 타입 추론 결과 */
export interface OracleTypeDef {
  dataType: string;
  nullable: boolean;
}

/** DDL 생성 결과 */
export interface CreateTableResult {
  ddl: string;
  commentsDDL: string[];
  columns: Array<{ columnName: string; dataType: string; nullable: boolean; isSystem: boolean; sourceField: string; comment: string }>;
}

/** 프로시져 DDL 생성 결과 */
export interface CreateProcedureResult {
  ddl: string;
  params: Array<{ paramName: string; dataType: string; sourceField: string }>;
}

